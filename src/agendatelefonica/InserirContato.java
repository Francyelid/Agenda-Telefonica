    private void txtNomeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyPressed
        int tamMax = 20;
        
        txtNome.setText(txtNome.getText().length() <= tamMax ? txtNome.getText() : txtNome.getText().substring(0, tamMax));
        
        lblTam.setText(Integer.toString((tamMax - txtNome.getText().length())));
    }//GEN-LAST:event_txtNomeKeyPressed

