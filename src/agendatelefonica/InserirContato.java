    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(txtNome.getText().length() != 0 && !(txtNumero.getText().contains("  "))){
            FileWriter writer;
            try {
                writer = new FileWriter("listaAgenda.txt", true);
                writer.write(txtNome.getText() + " | " + txtNumero.getText() + "\n");
                writer.close();
            } catch (IOException ex) {
                Logger.getLogger(InserirContato.class.getName()).log(Level.SEVERE, null, ex);
            }

            FechaJanela();
        }
        else{
            lblCampVaz.setVisible((txtNumero.getText().contains("  ") ? true : false));
            lblCampVaz2.setVisible((txtNome.getText().length() == 0 ? true : false));
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

